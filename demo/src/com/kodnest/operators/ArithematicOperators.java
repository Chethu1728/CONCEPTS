package com.kodnest.operators;

public class ArithematicOperators {

	public static void main(String[] args) {
		int a=50;
		int b=20;
		System.out.println("addition result is  "+(a+b));
		System.out.println("subtraction result is  "+(a-b));
		System.out.println("mulplication result is  "+(a*b));
		System.out.println("quotient of a division is  "+(a/b));
		System.out.println("reminder of a division is  "+(a%b));
	}

}
