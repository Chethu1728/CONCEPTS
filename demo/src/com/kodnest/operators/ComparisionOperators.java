package com.kodnest.operators;

public class ComparisionOperators {

	public static void main(String[] args) {
		int a=20;
		int b=30;
		int c=20;
		System.out.println("is a equal to c? "+(a==c));
		System.out.println("is a is greater than b? "+(a>b));
		System.out.println("is a is lesser than b? "+(a<b));
		System.out.println("is a is greater than or equal to b? "+(a>=b));
		System.out.println("is a is not equal to b? "+(a!=b));
	}

}
